package simulador.forms;

import java.util.List;

public class BaseForm {
	
	
	protected List codigoPracticasList;
	protected List descripcionPracticasList;
	protected List  piezaList;
	protected List carasList;
	
	
	public List getCodigoPracticasList() {
		return codigoPracticasList;
	}
	public void setCodigoPracticasList(List codigoPracticasList) {
		this.codigoPracticasList = codigoPracticasList;
	}
	public List getDescripcionPracticasList() {
		return descripcionPracticasList;
	}
	public void setDescripcionPracticasList(List descripcionPracticasList) {
		this.descripcionPracticasList = descripcionPracticasList;
	}
	public List getPiezaList() {
		return piezaList;
	}
	public void setPiezaList(List piezaList) {
		this.piezaList = piezaList;
	}
	public List getCarasList() {
		return carasList;
	}
	public void setCarasList(List carasList) {
		this.carasList = carasList;
	}
	
	
	
	
	
	
	

}
